export const add = (num1, num2) => {
  console.log(`${num1} + ${num2} = ${num1 + num2}`)
}

export const sub = (num1, num2) => {
  console.log(`${num1} - ${num2} = ${num1 - num2}`)
}

export const multi = (num1, num2) => {
  console.log(`${num1} * ${num2} = ${num1 * num2}`)
}

export const divide = (num1, num2) => {
  console.log(`${num1} / ${num2} = ${num1 / num2}`)
}
